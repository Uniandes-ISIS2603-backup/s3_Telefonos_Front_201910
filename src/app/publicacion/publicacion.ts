/**
 * Esta clase representa una publicación de CambiaPhone.
 */
export class Publicacion {

    /**
    * fecha de creación del la publicacion
    */
   fechaPublicacion: string;

    /**
    * array con el conjunto de imagenes de la publicación
    */
   imagenes: string[];
   
   /**
    * El id de la publicacion
    */
   id: number;

   /**
    * El precio de la publicacion
    */
   precio: number;

}
