/**
 * Esta clase representa un proveedor de CambiaPhone. 
 * Contiene toda la informacion relevante de un proveedor.
 */
export class Proveedor {
    /**
     * Uusario del proveedor
     */
    usuario:String;

    /**
     * Contrasenia del proveedor
     */
    contrasenia: String;

    /**
     * El id del proveedor
     */
    id: number;

    /**
     * El correo electronico del proveedor
     */
    correoElectronico:String; 

    /**
     * El raiting del proveedor
     */
    raiting: number;

    /**
     * El nombre del proveedor
     */
    nombre:String

}
